#include "libft.h"

void	ft_bzero(void *s, size_t len)
{
	size_t	a;

	a = 0;
	if (len != 0)
	{
		while (a < len)
		{
			((unsigned char *)s)[a] = 0;
			a++;
		}
	}
}
